    const mongodb = require('mongodb');
    const objectId = mongodb.ObjectId;

    const getDB = require('../utils/db/database-mongodb').getDB;
    class User 
    {
        constructor(name, email)
        {
            this.name = name;
            this.email = email;
        }

        // create new user
        postUser()
        {
            const db = getDB();
            return db.collection('users')
                .insertOne(this)
                    .then( (result) => {} )
                    .catch( (err) => {} )
        }

        // fetch all users
        static getUsers()
        {
            const db = getDB();
            return db.collection('users')
                .find()
                .toArray()
					.then( (users) => {
						return users;
					} )
					.catch( (err) => {} )
        }

        // fetch individual users
        static getUser(id)
        {
            const db = getDB();
            return db.collection('users')
                .findOne( {_id : new objectId(String(id))} )
					.then( (user) => {
						return user;
					} )
					.catch( (err) => {} )
        }
    }


