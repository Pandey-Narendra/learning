    const path = require('path');

    const express = require('express');

    const rootDir = require('./utils/path');

    const bodyParser = require('body-parser');
   

    const app = express();
    app.use(bodyParser.urlencoded({ extended: false }));
    app.use(express.static(path.join(rootDir, 'public')));

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    //      Import Files Ans Set Data Starts
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
  


// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    // View Engine Configuration Starts
// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    // -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    // EJS Configuration starts
    // -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

        app.set('view engine', 'ejs');

        app.set('views', path.join(rootDir, 'views'));

    // -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    // EJS Configuration starts
    // -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    // View Engine Configuration Ends
// -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------



// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                //  Import Routes Starts
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    const adminRoutes = require('./routes/admin');

    const shopRoutes = require('./routes/shop');

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                    //  Import Routes ends
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                        //      Middleware Starts
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    // console.log('app.js 63', adminRoutes );

    app.use('/admin', adminRoutes);

    // console.log('app.js 65');

    app.use(shopRoutes);

    // console.log('app.js 69');

    // ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                                //      Controllers Starts
    // ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        
        const errorController = require('./controllers/errors');

        app.use('/', errorController.error404Page);

    // ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                            //      Controllers Ends
    // ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
     
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
                            //      Middleware Ends
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

app.listen(3000);